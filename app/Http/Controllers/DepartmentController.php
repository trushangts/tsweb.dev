    {
        //dd($request->input());
        $department = new Departments();
        $data = $this->validate($request, [
            'name'=>'required',
            'status'=> 'required'            
        ]);
       
        $department->saveDepartment($data);
        return redirect('/home')->with('success', 'Department Added Sucessfully.');
    }

